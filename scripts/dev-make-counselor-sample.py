#!/usr/bin/env python3
"""
造一份"辅导员角色赋予"测试样本 .xlsx。

目标字段（系统侧 COUNSELOR schema）：
  必填：工号 (username) / 教师姓名 (real_name) / 从属单位 (org_name)
  可选：角色 (role_code，默认 'counselor')

这一场景的语义：
  ① 工号必须已经在 sys_user 里存在（先用 教师基础信息 场景导进来）
  ② 给已存在的 user 加一行 sys_user_role(role='counselor', org_id=<从属单位>)
  ③ 从属单位命中现有 org_unit 复用；命不中 → 当机关部门 (admin_dept) 顺手建

⚠ 当前 Phase D **还没实现辅导员场景的执行器**:
   Step 1-4 能走，但 Step 5 "开始导入" 会返回 IMPORT_NOT_IMPLEMENTED。
   这份样本可以验证列映射 / 校验逻辑，等执行器实现后立即可用。

埋的考点（要先跑过 教师基础信息-测试样本.xlsx，让这些工号在 sys_user 里存在）：
- 大部分工号沿用教师样本里的（T2025001-T2025020）
- 第 5 行 "T9999999" → sys_user 里不存在 → 应报错 (执行器实现后)
- 第 9 行 工号空 → 必填校验报错
- 第 12 行 从属单位 = "学工部" → 系统里没有，顺手建机关部门
- 第 13 行 从属单位 = "研工部" → 同上
- 第 14 行 显式指定角色 = "college_admin" → 用 role_code 覆盖默认
- 第 16 行 工号重复 (跟第 1 行同) → 文件内重复校验

跑法：
  python3 scripts/dev-make-counselor-sample.py
  → 生成 scripts/辅导员角色赋予-测试样本.xlsx
"""
from openpyxl import Workbook
from pathlib import Path

HEADERS = ["工号", "姓名", "从属单位", "角色", "备注"]

ROWS = [
    # 1-4: 命中现有学院 (前提是先导过教师样本)
    ["T2025001", "陈志远", "计算机学院", "辅导员", ""],
    ["T2025002", "王慧敏", "计算机学院", "辅导员", ""],
    ["T2025004", "刘晓燕", "数学学院",   "辅导员", ""],
    ["T2025011", "吴文博", "文学院",     "辅导员", ""],
    # 5: 工号在系统里不存在 → 应报错
    ["T9999999", "幽灵教师", "计算机学院", "辅导员", "工号系统里没有"],
    # 6-8: 常规
    ["T2025007", "孙正阳", "环境科学学院", "辅导员", ""],
    ["T2025009", "赵海涛", "经济管理学院", "辅导员", ""],
    ["T2025013", "郑思琪", "文学院",     "辅导员", ""],
    # 9: 工号空 → 必填报错
    ["",         "无名氏",  "计算机学院", "辅导员", "工号空"],
    # 10-11: 同一个老师重复出现 (UPSERT 第二行的 org_id 会胜出)
    ["T2025005", "张伟明", "数学学院",   "辅导员", ""],
    ["T2025005", "张伟明", "学工部",     "辅导员", "同人不同单位 → 后面胜出"],
    # 12-13: 单位不存在 → 顺手建 admin_dept
    ["T2025014", "冯青松", "学工部",     "辅导员",  "机关部门待建"],
    ["T2025015", "卫晓彤", "研工部",     "辅导员",  "机关部门待建"],
    # 14: 显式指定别的角色 (覆盖默认 counselor)
    ["T2025003", "李建国", "计算机学院", "院系管理员", "院系管理员而不是辅导员"],
    # 15: 必填都全, 普通行
    ["T2025017", "杨慧颖", "经济管理学院", "辅导员", ""],
    # 16: 工号跟第 1 行重复 → 文件内重复校验
    ["T2025001", "陈志远", "学工部",     "辅导员", "工号文件内重复"],
    # 17-18: 教务处 (顺手建机关部门)
    ["T2025018", "朱秀英", "教务处",     "辅导员", "教务处也是新机关部门"],
    ["T2025019", "秦明远", "教务处",     "辅导员", ""],
    # 19-20: 正常
    ["T2025020", "尤美琳", "数学学院",   "辅导员", ""],
    ["T2025016", "韩立诚", "计算机学院", "辅导员", ""],
]


def main() -> None:
    wb = Workbook()
    ws = wb.active
    ws.title = "辅导员角色赋予"
    ws.append(HEADERS)
    for r in ROWS:
        ws.append(r)
    for i, h in enumerate(HEADERS, start=1):
        col = ws.cell(row=1, column=i).column_letter
        ws.column_dimensions[col].width = max(12, len(h) * 2 + 2)
    out = Path(__file__).parent / "辅导员角色赋予-测试样本.xlsx"
    wb.save(out)
    print(f"OK: {out}  ({len(ROWS)} 行)")


if __name__ == "__main__":
    main()
